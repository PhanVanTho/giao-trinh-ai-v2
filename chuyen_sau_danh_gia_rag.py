baseline_prompt = """You are an expert evaluator for AI-generated educational textbooks.
You are evaluating a curriculum outline on "{topic}".

Score from 0.0 to 1.0 for each metric:
1. Context Relevance: Is the content focused on the topic?
2. Faithfulness: Is the factual content accurate?
3. Answer Correctness: Is the curriculum structure appropriate?
4. Coherence: Is the logical flow well-structured?
5. Consistency: Is the depth and tone consistent?

CONTENT:
{text}

Return ONLY:
Context Relevance: X.XX
Faithfulness: X.XX
Answer Correctness: X.XX
Coherence: X.XX
Consistency: X.XX"""

def eval_baseline_folder(folder, label):
    files = glob.glob(os.path.join(folder, '*.txt'))
    res = []
    print(f"\n--- Evaluating {label} ({len(files)} files) ---")
    for f in files:
        try:
            topic = os.path.basename(f).replace('.txt','')
            content = open(f, 'r', encoding='utf-8').read()
            if len(content) < 100: continue
            
            prompt = baseline_prompt.format(topic=topic, text=content[:6000])
            response = model.generate_content(prompt, generation_config=genai.types.GenerationConfig(temperature=0.0))
            reply = response.text
            
            cr = float(re.search(r'Context Relevance:\s*([\d\.]+)', reply).group(1))
            fa = float(re.search(r'Faithfulness:\s*([\d\.]+)', reply).group(1))
            ac = float(re.search(r'Answer Correctness:\s*([\d\.]+)', reply).group(1))
            co = float(re.search(r'Coherence:\s*([\d\.]+)', reply).group(1))
            cs = float(re.search(r'Consistency:\s*([\d\.]+)', reply).group(1))
            
            print(f"  [{topic}] CR={cr:.2f} FA={fa:.2f} AC={ac:.2f} CO={co:.2f} CS={cs:.2f}")
            res.append((cr, fa, ac, co, cs))
        except Exception as e:
            print(f"  [{topic}] ERROR: {e}")
        time.sleep(1)
    
    if res:
        avg = [sum(x)/len(res) for x in zip(*res)]
        return avg
    return None

score_zs = eval_baseline_folder(r"d:\tu_dong_giao_trinh\ThucNghiem_KetQua\Baseline_ZeroShot", "Zero-shot LLM")
score_rag = eval_baseline_folder(r"d:\tu_dong_giao_trinh\ThucNghiem_KetQua\Baseline_NaiveRAG", "Naive RAG")

